package EjBasicos3T.Semana4.Ej2;

public class Personaje {

    public String nombre;
    public int ataque;

    public Personaje(String nombre, int ataque) {
        this.nombre = nombre;
        this.ataque = ataque;
    }

    public int atacar() {

        return this.atacar();

    }

    public String getNombre() {

        return nombre;

    }

    public int getAtaque() {

        return ataque;

    }
}


